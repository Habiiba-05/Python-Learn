# Arithmetic operators

# '+'  add
num = 2 
num2 = 8
print(num + num2)

# '*'  multiplication
num1 = 1
num2 = 5
total = num1 * num2
print(total) 

# '-'  subtraction 
num = 2 
num2 = 8
print(num2 - num)

# '/'  division
num = 2 
num2 = 8
print(num2 / num)

# '//'  division but convert a decimal num to an integar 
num = 10
num2 = 3
print(num // num2)

# '%'  modulos, return the remainder of division 
# Important in (odd/even) nums
num = 10
num2 = 3
print(num % num2)

# '**'  power = exponentation
num = 10
num2 = 3
print(num ** num2)

