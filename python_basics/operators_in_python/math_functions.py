# Math functions 

# round() function
print(round(2.4))  # rounding down
print(round(2.7))  # rounding up
 
# absolute() this func returns the num positive all the time even it's negative
print(abs(10))
print(abs(-15)) 

# This allows us to use all math functions
import math 
print(math.ceil(2.4))  # After '.' show all math operators on math module
print(math.floor(2.4))
# ceil - the largest integer to x,   2.4 = 3
# floor - the smallest integer to x,  2.4 = 2